  /* ============================================================
     CARD BUILDER SYSTEM
     ============================================================ */
  function initializeMatrix() {
    const mesh = document.getElementById('vector-mesh');
    modulesRegistry.forEach((mod, index) => {
      const node = document.createElement('div');
      node.className = `vector-node ${mod.uiClass}`;
      node.innerHTML = `
        <span class="vn-idx">VECTOR NODE ${mod.id}</span>
        <div class="vn-glyph">${mod.glyph}</div>
        <div class="vn-heading">${mod.heading}</div>
        <div class="vn-abstract">${mod.abstract}</div>
        <div class="vn-trigger-arrow">⚙️</div>
      `;
      node.addEventListener('click', () => {
        node.classList.add('activated-click');
        setTimeout(() => parseStream(index), 100);
      });
      mesh.appendChild(node);
    });
  
    // Capstone row configuration
    const capstoneContainer = document.getElementById('capstone-row');
    const capstoneNode = document.createElement('div');
    capstoneNode.className = 'capstone-node-card';
    capstoneNode.innerHTML = `
      <div class="cn-glyph-box">${capstoneProject.icon}</div>
      <div class="cn-meta">
        <span class="cn-badge">✦ Applied Capstone Vector</span>
        <div class="cn-title">${capstoneProject.title}</div>
        <div class="cn-desc">${capstoneProject.short}</div>
      </div>
      <div class="cn-action">DEPLOY</div>
    `;
    capstoneNode.addEventListener('click', () => {
      capstoneNode.classList.add('activated-click');
      setTimeout(() => parseCapstoneStream(), 100);
    });
    capstoneContainer.appendChild(capstoneNode);
  }
  
  /* ============================================================
     INTERFACE VIEW SHIFTER
     ============================================================ */
  function switchView(viewId) {
    document.querySelectorAll('.view-panel').forEach(panel => {
      panel.classList.remove('active-view');
      panel.classList.add('hidden-view');
    });
    const targetView = document.getElementById(viewId);
    targetView.classList.remove('hidden-view');
    targetView.classList.add('active-view');
    targetView.scrollTop = 0;
    
    // Linear progress tracker control
    document.getElementById('topProgress').style.width = '0%';
  }
  
  /* ============================================================
     MODULE STREAM PARSER
     ============================================================ */
  function parseStream(index) {
    activeStreamIndex = index;
    streamingCapstone = false;
    const mod = modulesRegistry[index];
  
    document.getElementById('module-index-flag').textContent = `SYSTEM NODE ${mod.id}`;
    document.getElementById('console-main-title').textContent = mod.heading;
    document.getElementById('console-sub-title').textContent = mod.abstract;
    document.getElementById('hero-watermark').textContent = mod.id;
    document.getElementById('console-tracker').textContent = `Module ${index + 1} / ${modulesRegistry.length}`;
  
    assembleConsoleOutput(mod.tagList, mod.markupPayload);
    switchView('console-view');
  }
  
  function parseCapstoneStream() {
    streamingCapstone = true;
    document.getElementById('module-index-flag').textContent = 'SYSTEM CAPSTONE SPRINT';
    document.getElementById('console-main-title').textContent = capstoneProject.title;
    document.getElementById('console-sub-title').textContent = capstoneProject.short;
    document.getElementById('hero-watermark').textContent = '★';
    document.getElementById('console-tracker').textContent = 'Capstone Node';
    
    assembleConsoleOutput(capstoneProject.tags, capstoneProject.markupPayload);
    switchView('console-view');
  }
  
  function assembleConsoleOutput(tags, payloadMarkup) {
    const generatedTags = tags.map(t => `<span class="console-tag-node">${t}</span>`).join('');
    document.getElementById('console-body-payload').innerHTML = `
      ${payloadMarkup}
      <div class="console-tag-cloud-wrapper">${generatedTags}</div>
    `;
  }
  
  /* ============================================================
     PAGINATION STREAM CONTROLLER
     ============================================================ */
  function shiftStream(direction) {
    if (streamingCapstone) {
      if (direction === -1) parseStream(modulesRegistry.length - 1);
      return;
    }
    const computedNext = activeStreamIndex + direction;
    if (computedNext < 0) { 
      switchView('dashboard-view'); 
      return; 
    }
    if (computedNext >= modulesRegistry.length) { 
      parseCapstoneStream(); 
      return; 
    }
    parseStream(computedNext);
  }
  
  /* ============================================================
     VIEWPORTS ANCHOR NAVIGATION
     ============================================================ */
  function jumpTo(elementId) {
    const container = document.getElementById('dashboard-view');
    const targetElement = document.getElementById(elementId);
    if (targetElement) {
      container.scrollTo({
        top: targetElement.offsetTop - 70,
        behavior: 'smooth'
      });
    }
  }
  
  /* ============================================================
     POINTER LIGHT EFFECTS & TOP SCROLL STATUS TRACKER
     ============================================================ */
  function setupGlobalFX() {
    const glow = document.getElementById('pointerGlow');
    document.addEventListener('mousemove', e => {
      glow.style.left = `${e.clientX}px`;
      glow.style.top = `${e.clientY}px`;
    });
  
    const dashboard = document.getElementById('dashboard-view');
    const progressBar = document.getElementById('topProgress');
    
    dashboard.addEventListener('scroll', () => {
      const totalScrollableDistance = dashboard.scrollHeight - dashboard.clientHeight;
      if (totalScrollableDistance > 0) {
        const scrollPercentage = (dashboard.scrollTop / totalScrollableDistance) * 100;
        progressBar.style.width = `${scrollPercentage}%`;
      }
    });
  }
  
  /* ============================================================
     SYSTEM RUNTIME INITIALIZATION
     ============================================================ */
  document.addEventListener('DOMContentLoaded', () => {
    initializeMatrix();
    setupGlobalFX();
  });